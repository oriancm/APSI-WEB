                <h1>Nos Métiers</h1>

                <h2>Ordonnancement, Pilotage et Coordination (OPC)</h2>

                <h3>Définition de la mission OPC</h3>
                <p>L’Ordonnancement, Pilotage et Coordinationn, plus connue sous son acronyme OPC, est une mission clé en phase de chantier, aussi bien pour des projets de construction que de rénovation. Son objectif est d’assurer l’organisation et le suivi au quotidien des entreprises en charge de l’exécution d’un ou plusieurs lots techniques, afin de respecter les délais du projet.</p>
                <p>Cette étape importante est définie et encadrée par un texte de loi, le décret n°93-1268 qui indique que :</p>
                <p>« L’ordonnancement, la coordination et le pilotage du chantier ont respectivement pour objet :</p>
                <ul>
                    <li>d’analyser les tâches élémentaires portant sur les études d’exécution et les travaux, de déterminer leurs enchaînements ainsi que leur chemin critique,</li>
                    <li>d’harmoniser dans le temps et dans l’espace les actions des différents intervenants ;</li>
                    <li>au stade des travaux et jusqu’à la levée des réserves, de mettre en application les diverses mesures au titre de l’ordonnancement et de la coordination. »</li>
                </ul>

                <img src="img\chantier.jpg" alt="Chantier" class="chantier-image">

                <h3>En quoi consiste la mission OPC ?</h3>
                <p>La mission OPC consiste, comme son nom l’indique, à ordonner, piloter et coordonner un projet :</p>
                <ul>
                    <li><strong>L’ordonnancement</strong> représente une phase plutôt amont lors de laquelle le coordonnateur OPC va participer aux études de faisabilité. L’objectif est d’analyser chaque tâche, les demandes administratives à obtenir, les contraintes naturelles du projet. Ce travail va permettre de commencer à établir un planning prévisionnel afin de bien gérer l’exécution des travaux et l’enchaînement des différents prestataires, dépendants de l’avancée des uns et des autres. Enfin cette étape va permettre d’avoir une idée un peu plus fine de la date de livraison prévue.</li>
                    
                    <img src="img\gantt.png" alt="Chantier" class="chantier-image">

                    <li><strong>Le pilotage</strong> est la gestion quotidienne des travaux jusqu’à la livraison du projet et la levée des réserves.</li>
                    <li><strong>La coordination</strong> est une mission qui s’opère au quotidien avec les multiples intervenants qui vont intervenir sur l’opération :
                        <ul>
                            <li>l’Architecte,</li>
                            <li>les Bureaux d’Etudes Techniques (fluides, structures, sols, amiante et plomb, etc…)</li>
                            <li>le Bureau de Contrôle,</li>
                            <li>le Coordonnateur SPS,</li>
                            <li>les entreprises titulaires des marchés de travaux,</li>
                            <li>le chef d’établissement et les exploitants dans le cas d’interventions sur un site occupé et en activité,</li>
                            <li>les concessionnaires des réseaux publics sur lesquelles doivent se raccorder le projet.</li>
                        </ul>
                        Elle découle naturellement de la phase d’ordonnancement car elle s’appuie sur le planning qui aura été fait. Toutefois, c’est une étape un peu plus opérationnelle car elle va gérer le projet selon son avancée réelle.
                    </li>
                </ul>

                <h3>Quand commence-t-on la mission OPC ?</h3>
                <p>La mission d’Ordonnancement Pilotage et Coordination commence dès la phase de conception du projet par l’équipe de maîtrise d’œuvre.</p>
                <p>En effet, il est pertinent de consulter le coordonnateur OPC du projet pour prévoir dès lors un calendrier général de l’opération et ainsi commencer à identifier une date de livraison du projet.</p>

                <h3>Les grandes étapes de la mission OPC</h3>
                <ol>
                    <li><strong>PHASE DE CONCEPTION</strong><br>
                        (avant la consultation des entreprises)
                        <ul>
                            <li>établissement et mises à jour du calendrier général de l’opération (depuis la phase DIAG/APS jusqu’à la livraison de l’opération, et incluant la phase de consultation des entreprises et celle relative au Permis de Construire ou d’Aménager),</li>
                            <li>calendrier enveloppe par corps d’état,</li>
                            <li>établissement d’un plan de principe d’organisation et d’installation de chantier soumis à l’avis du Coordonnateur SPS,</li>
                            <li>assistance à la rédaction des documents contractuels des marchés de travaux (AE, CCAP, CCTP, etc…) en matière d’organisation et de délai,</li>
                            <li>assistance à l’élaboration du carnet de phasage dans le cas d’opération de réhabilitation en site occupé avec maintien de l’activité (y compris indication des contraintes fortes d’organisation à intégrer dans les documents de consultation des entreprises),</li>
                            <li>participation à des réunions en phase conception avec les différents intervenants du projet (maître d’ouvrage, Architecte, bureaux d’études techniques, contrôleur technique, coordonnateur SPS, etc…) de manière à bien assimiler les caractéristiques des travaux projetés et appréhender les contraintes spécifiques en matière d’organisation générale du chantier.</li>
                        </ul>
                    </li>
                    <img src="img\gantt2.png" alt="Chantier" class="chantier-image">

                    <li><strong>PERIODE DE PREPARATION DE CHANTIER</strong>
                        <ul>
                            <li>élaboration du calendrier détaillé des études d’exécution avec les dates incombant à chaque intervenant,</li>
                            <li>suivi du calendrier des études d’exécution,</li>
                            <li>établissement de rapports périodiques d’avancement des études d’exécution,</li>
                            <li>participation et animation des réunions de préparation,</li>
                            <li>diffusion des comptes-rendus.</li>
                        </ul>
                    </li>
                    <li><strong>PHASE TRAVAUX</strong>
                        <ul>
                            <li>animation des réunions de coordination, rédaction et diffusion des compte-rendus,</li>
                            <li>pointage hebdomadaire des différentes interventions, recensement des écarts constatés et repérage de leur origine,</li>
                            <li>proposition de mesures correctives pour rattraper les retards,</li>
                            <li>recalage des calendriers en fonction des besoins,</li>
                            <li>contrôle de l’entretien et du nettoyage du chantier,</li>
                            <li>appréciation des responsabilités concernant les retards constatés et proposition d’application de pénalités éventuelles.</li>
                        </ul>
                    </li>
                    <li><strong>OPERATIONS DE RECEPTION</strong>
                        <ul>
                            <li>Etablissement de calendriers détaillés faisant notamment apparaître :
                                <ul>
                                    <li>les Opérations Préalables à la Réception,</li>
                                    <li>les essais de mise en service, vérifications techniques,</li>
                                    <li>les opérations de réception et levées de réserves,</li>
                                    <li>les visites des commissions de sécurité,</li>
                                    <li>la fourniture des Dossiers des Ouvrages Exécutés (DOE),</li>
                                    <li>le repliement des installations de chantier et la remise en état des lieux.</li>
                                </ul>
                            </li>
                            <li>animation des réunions pendant la période de levée des réserves, rédaction et diffusion des compte-rendus.</li>
                        </ul>
                    </li>
                </ol>

                <h3>Les atouts de la mission OPC chez APSI BTP :</h3>
                <ul>
                    <li>la garantie du respect des délais et des objectifs fixés,</li>
                    <li>un travail d’équipe en étroite collaboration avec l’Architecte et/ou le Maître d’Œuvre, mais également avec les autres intervenants (représentant du maître d’ouvrage, contrôleur technique, coordonnateur SPS, bureaux d’études techniques, concessionnaires, etc…),</li>
                    <li>une forte implication au quotidien pour gérer et résoudre les nombreuses difficultés et aléas qui ne manqueront pas d’apparaître tout au long de l’exécution des travaux,</li>
                    <li>l’anticipation de nombreux sujets de manière à faciliter et fluidifier le bon déroulement des travaux (à l’instar d’un « démineur » qui prépare l’avancée des troupes),</li>
                    <li>une grande disponibilité suivi d’une forte réactivité,</li>
                    <li>une capacité d’adaptation élevée en fonction des changements voulus par nos clients ou imposés par des évènements extérieurs (intempéries, intervention à l’intérieur d’un site en activité, rupture d’approvisionnement par les fournisseurs, grèves, etc…),</li>
                    <li>l’assurance d’une planification détaillée, ambitieuse et réaliste,</li>
                    <li>des compétences techniques reconnues qui permettent d’orienter, en collaboration avec l’Architecte et/ou le Maître d’Œuvre, le choix des méthodes de construction dans le seul but d’optimiser le délai d’exécution des travaux en évitant également des frais supplémentaires,</li>
                    <li>des relations humaines saines et respectueuses avec les différents intervenants,</li>
                    <li>des comptes-rendus clairs et précis des réunions hebdomadaires,</li>
                    <li>la traçabilité des décisions prises en réunion et de leurs retranscriptions sur le terrain,</li>
                    <li>un accompagnement possible pendant la période de Garantie de Parfait Achèvement.</li>
                </ul>

                <h2>Maîtrise d’Œuvre d’Exécution (MOEX)</h2>

                <h3>Définition de la mission MOEX</h3>
                <p>La Maîtrise d’Œuvre d’Exécution (MOEX), ou Direction de l’Exécution des Travaux (DET), consiste à s’assurer que les travaux respectent les plans, les matériaux et les normes réglementaires. Elle intervient dès l’ouverture du chantier jusqu’à sa fermeture, en supervisant les travaux et en assurant le lien entre la maîtrise d’ouvrage et les entreprises.</p>

                <h3>Prestations de la mission MOEX chez APSI BTP</h3>
                <ul>
                    <li>Suivi et direction des travaux.</li>
                    <li>Contrôle de la qualité des travaux.</li>
                    <li>Respect des normes de construction (DTU, Normes Françaises, Avis Techniques).</li>
                    <li>Coordination des travaux entre les entreprises.</li>
                    <li>Veille au respect des consignes d’hygiène et de sécurité.</li>
                    <li>Tenue des réunions hebdomadaires de chantier.</li>
                    <li>Gestion financière du chantier.</li>
                    <li>Assistance au maître d’ouvrage jusqu’à la réception et la levée des réserves.</li>
                    <li>Vérification des décomptes généraux et définitifs (DGD).</li>
                    <li>Collecte et vérification des dossiers des ouvrages exécutés (DOE).</li>
                    <li>Assistance pendant l’année de parfait achèvement.</li>
                </ul>

                <h3>Les avantages de la mission MOEX chez APSI BTP</h3>
                <ul>
                    <li>Garantie du respect des objectifs fixés.</li>
                    <li>Forte implication quotidienne pour gérer les aléas.</li>
                    <li>Grande disponibilité.</li>
                    <li>Gestion financière rigoureuse.</li>
                    <li>Capacity d’adaptation face aux changements ou imprévus.</li>
                    <li>Compétences techniques pour optimiser les délais et maîtriser les coûts.</li>
                    <li>Relations humaines respectueuses.</li>
                    <li>Comptes-rendus clairs et précis.</li>
                    <li>Traçabilité des décisions prises.</li>
                    <li>Accompagnement pendant la garantie de parfait achèvement.</li>
                </ul>
